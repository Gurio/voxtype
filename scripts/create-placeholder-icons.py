#!/usr/bin/env python3
"""
Create minimal placeholder icons for Tauri app.
Run this if ImageMagick is not available.
"""

import os
from pathlib import Path

# Minimal 1x1 PNG (transparent)
PNG_1X1 = bytes([
    0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, 0x00, 0x00, 0x00, 0x0D,
    0x49, 0x48, 0x44, 0x52, 0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01,
    0x08, 0x06, 0x00, 0x00, 0x00, 0x1F, 0x15, 0xC4, 0x89, 0x00, 0x00, 0x00,
    0x0A, 0x49, 0x44, 0x41, 0x54, 0x78, 0x9C, 0x63, 0x00, 0x01, 0x00, 0x00,
    0x05, 0x00, 0x01, 0x0D, 0x0A, 0x2D, 0xB4, 0x00, 0x00, 0x00, 0x00, 0x49,
    0x45, 0x4E, 0x44, 0xAE, 0x42, 0x60, 0x82
])

# Minimal ICO file (16x16, 1-bit)
ICO_MINIMAL = bytes([
    0x00, 0x00, 0x01, 0x00, 0x01, 0x00, 0x10, 0x10, 0x00, 0x00, 0x01, 0x00,
    0x20, 0x00, 0x68, 0x04, 0x00, 0x00, 0x16, 0x00, 0x00, 0x00, 0x28, 0x00,
    0x00, 0x00, 0x10, 0x00, 0x00, 0x00, 0x20, 0x00, 0x00, 0x00, 0x01, 0x00,
    0x20, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x04, 0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    0x00, 0x00
] + [0x63, 0x66, 0xF1, 0xFF] * 256)  # Indigo color

def create_icons():
    icon_dir = Path(__file__).parent.parent / "src-tauri" / "icons"
    icon_dir.mkdir(parents=True, exist_ok=True)
    
    # Create PNG placeholders
    for name in ["32x32.png", "128x128.png", "128x128@2x.png"]:
        path = icon_dir / name
        print(f"Creating {name}...")
        with open(path, "wb") as f:
            f.write(PNG_1X1)
    
    # Create ICO
    ico_path = icon_dir / "icon.ico"
    print(f"Creating icon.ico...")
    with open(ico_path, "wb") as f:
        f.write(ICO_MINIMAL)
    
    # Create ICNS (macOS) - just a renamed PNG for now
    icns_path = icon_dir / "icon.icns"
    print(f"Creating icon.icns...")
    with open(icns_path, "wb") as f:
        f.write(PNG_1X1)
    
    print("\n✓ Placeholder icons created!")
    print("⚠ These are minimal placeholders. For production, use proper icons.")
    print("  Run scripts/generate-icons.sh if you have ImageMagick installed.")

if __name__ == "__main__":
    create_icons()

